namespace Samples.AspCoreEF.Infrastructure.Services
{
    public class AuthMessageSender : IEmailSender, ISmsSender
    {
        public AuthMessageSender(IOptions<AuthMessageSenderOptions> optionsAccessor)
        {
            Options = optionsAccessor.Value;
        }

        public AuthMessageSenderOptions Options { get; } //set only via Secret Manager

        public Task SendEmailAsync(string email, string subject, string content)
        {
            // Plug in your email service here to send an email.

            var message = new MimeMessage();
            message.From.Add(new MailboxAddress("Tuan Duong", "duongtuan1211@gmail.com"));
            message.To.Add(new MailboxAddress("Sending Test", email));
            message.Subject = subject;
            message.Body = new TextPart("Html") { Text = content };
            using (var client = new SmtpClient())
            {
                // For demo-purposes, accept all SSL certificates (in case the server supports STARTTLS)
                client.ServerCertificateValidationCallback = (s, c, h, e) => true;

                client.Connect("smtp.gmail.com", 587, false);

                // Note: since we don't have an OAuth2 token, disable
                // the XOAUTH2 authentication mechanism.
                client.AuthenticationMechanisms.Remove("XOAUTH2");

                // Note: only needed if the SMTP server requires authentication
                client.Authenticate("duongtuan1211@gmail.com", "Tuan@123");

                client.Send(message);
                client.Disconnect(true);
            }
            return Task.FromResult(0);
        }

